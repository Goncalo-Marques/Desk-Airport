    private void readTablePiloto(Date d) {
        DefaultTableModel modelo1 = (DefaultTableModel) tablePiloto.getModel();
        modelo1.setNumRows(0);

        ConsultasDao consultasDao = new ConsultasDao();

        if (consultasDao.readPilotoVooData(d).isEmpty() == false) {
            for (Piloto p : consultasDao.readPilotoVooData(d)) {
                modelo1.addRow(new Object[]{
                    p.getIdPiloto(),
                    p.getNomeP(),
                    p.getMorada(),
                    p.getLocalidade(),
                    p.getIdade()
                });
            }
        } else {
            JOptionPane.showMessageDialog(null, "NÃ£o existem dados para esta pesquisa!");
        }

    }

